void DWT_Init(void)
{
CoreDebug->DEMCR |= CoreDebug_DEMCR_TRCENA_Msk;// enable trace
DWT->CYCCNT = 0;// reset counter
DWT->CTRL |= DWT_CTRL_CYCCNTENA_Msk;// enable cycle counter
}

static inline void delay_cycles(uint32_t cycles)
{
uint32_t start = DWT->CYCCNT;
while((DWT->CYCCNT - start) < cycles);
}

static inline void send_bit(uint8_t bit)
{
    if(bit)
    {
        LED_HIGH();
        delay_cycles(T1H_CYCLES);
        LED_LOW();
        delay_cycles(T1L_CYCLES);
    }
    else
    {
        LED_HIGH();
        delay_cycles(T0H_CYCLES);
        LED_LOW();
        delay_cycles(T0L_CYCLES);
    }
}

void send_led(uint8_t red, uint8_t green, uint8_t blue, uint8_t white)
{
    uint32_t color = 
        ((uint32_t)red << 24) |
        ((uint32_t)green   << 16) |
        ((uint32_t)blue  << 8)  |
        ((uint32_t)white);

    for(int8_t i = 31; i >= 0; i--)
    {
        send_bit((color >> i) & 1);
    }
}

void send_reset(void)
{
    LED_LOW();
    delay_cycles(RES_CYCLES);
}


